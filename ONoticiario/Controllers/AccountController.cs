        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model,HttpPostedFileBase avatarUpload)
        {
          
            ApplicationDbContext db = new ApplicationDbContext();
            var userManager = new UserManager<ApplicationUser>(new UserStore<ApplicationUser>(db));
            string nomeAvatar = "Avatar_"+ model.Utilizador.ID + ".jpg";
            string caminhoAvatar = "";
            //image por defeito
            model.Utilizador.Avatar = "default.png";
            //se houver imagem
            if (avatarUpload != null)
            {
                if (avatarUpload.ContentType.Contains("image"))
                {
                    model.Utilizador.Avatar = nomeAvatar;
                    caminhoAvatar = Path.Combine(Server.MapPath("~/imagens/Avatares/"), nomeAvatar);
                    avatarUpload.SaveAs(caminhoAvatar);
                }

            }
            if (model.Utilizador.DataNascimento >= System.DateTime.Now)
            {
                ModelState.AddModelError("", "Erro na data de nascimento!");
                return View(model);
            }
           
            if (ModelState.IsValid)
            {
                var user = new ApplicationUser { UserName = model.Email, Email = model.Email };
                var result = await UserManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    
                    Utilizadores utilizador = new Utilizadores {ID=db.Utilizadores.Max(a=>a.ID)+1, Nome = model.Utilizador.Nome, Descricao = model.Utilizador.Descricao,Avatar=model.Utilizador.Avatar, Username = model.Email,DataNascimento=model.Utilizador.DataNascimento};
                    db.Utilizadores.Add(utilizador);
                    db.SaveChanges();
                    var result1 = userManager.AddToRole(user.Id, "Utilizador");
                    TempData["Success"] = "Registado com Sucesso.";
                    return RedirectToAction("Login");
                    
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

        //
        // GET: /Account/ConfirmEmail
        [AllowAnonymous]
        public async Task<ActionResult> ConfirmEmail(string userId, string code)
        {
            if (userId == null || code == null)
            {
                return View("Error");
            }
            var result = await UserManager.ConfirmEmailAsync(userId, code);
            return View(result.Succeeded ? "ConfirmEmail" : "Error");
